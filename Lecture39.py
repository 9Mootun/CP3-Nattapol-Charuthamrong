Gradeinput = int(input("Enter your Score : "))
if Gradeinput >= 80 :
    print("Your Grade is A")
elif Gradeinput >= 75 and Gradeinput < 80:
    print("Your Grade is B+")
elif Gradeinput >= 70 and Gradeinput < 75:
    print("Your Grade is B")
elif Gradeinput >= 65 and Gradeinput < 70:
    print("Your Grade is C+")
elif Gradeinput >= 60 and Gradeinput < 65:
    print("Your Grade is C")
elif Gradeinput >= 55 and Gradeinput < 60:
    print("Your Grade is D+")
elif Gradeinput >= 50 and Gradeinput < 55:
    print("Your Grade is D")
elif Gradeinput < 50:
    print("Your Grade is F")